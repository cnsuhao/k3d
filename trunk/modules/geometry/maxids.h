#define MAXID_FRAME_PIVOT	0xB013
#define MAXID_FRAME_POSITION	0xB020
#define MAXID_FRAME_ROTATION	0xB021
#define MAXID_FRAME_SCALE	0xB022
#define MAXID_FRAME_HIERARCHY	0xB030


#endif // MAXIDS_H
