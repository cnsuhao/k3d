#define MAXID_AMBIENT		0xA010
#define MAXID_DIFFUSE		0xA020
#define MAXID_SPECULAR		0xA030
#define MAXID_SHININESS		0xA040
#define MAXID_SHINE_STRENGTH	0xA041
